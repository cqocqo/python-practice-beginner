# lists and strings can be broken down and made up of smaller pieces

# operations on strings
# addition sign concatenation
greeting = ' hello'
name = ' emeely'
print(greeting + name)

# * operator
print(greeting*3)
print((greeting + name)*3)

# index operator
Name = 'stark'
print(Name[2])
print(Name[0] + Name[3])

# slicing strings
print(Name[0:])
print(Name[:2])

# lower case and uppercase
Name = 'rogers'
print(Name.lower())